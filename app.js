function setupMap() {

  const map =
    document.getElementById("mapScreen")

  const title =
    document.getElementById("mapTitle")

  if (!map || !title) return

  // DAY 1

  if (state.currentDay === 1) {

    setBlurredBackground(
      map,
      "mapday1.png"
    )

    title.innerText =
      "Историческая Москва"

  }

  // DAY 2

  if (state.currentDay === 2) {

    setBlurredBackground(
      map,
      "mapday2.png"
    )

    title.innerText =
      "Современная Москва"

  }

}


// ======================================
// LIGHT SPOT
// ======================================

function moveSpot() {

  const spot =
    document.getElementById("spot")

  const map =
    document.getElementById("mapScreen")

  if (!spot || !map) return

  const x =
    Math.random() *
    (map.offsetWidth - 140)

  const y =
    Math.random() *
    (map.offsetHeight - 140)

  spot.style.left =
    x + "px"

  spot.style.top =
    y + "px"

}

setInterval(moveSpot, 2000)


