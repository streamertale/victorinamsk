// BLURRED BACKGROUNDS
// ======================================

function getBlurredImagePath(fileName) {

  const baseName =
    fileName.replace(/\.[^.]+$/, "")

  return `images/blur/${baseName}-blur.png`

}

function getOptimizedImagePath(fileName) {

  const baseName =
    fileName.replace(/\.[^.]+$/, "")

  return `images/optimized/${baseName}.jpg`

}

function setBlurredBackground(element, fileName) {

  if (!element || !fileName) return

  const fullPath =
    getOptimizedImagePath(fileName)

  const previewPath =
    getBlurredImagePath(fileName)

  element.classList.remove("bg-loaded")

  element.style.setProperty(
    "--bg-preview",
    `url("${previewPath}")`
  )

  element.style.setProperty(
    "--bg-full",
    `url("${fullPath}")`
  )

  const loadToken =
    `${fileName}-${Date.now()}`

  element.dataset.bgLoadToken =
    loadToken

  const image =
    new Image()

  image.onload = () => {

    if (
      element.dataset.bgLoadToken ===
      loadToken
    ) {

      element.classList.add("bg-loaded")

    }

  }

  image.src =
    fullPath

  if (image.complete) {

    element.classList.add("bg-loaded")

  }

}

function setupInitialBackgrounds() {

  setBlurredBackground(
    document.getElementById("startScreen"),
    "start-bg.png"
  )

}


