// ======================================
// RENDER QUESTION
// ======================================

function renderQuestion() {

  const q =
    state.currentQuestion

  if (!q) return

  // background

  const questionScreen =
    document.getElementById(
      "questionScreen"
    )

  if (questionScreen) {

    setBlurredBackground(
      questionScreen,
      q.background
    )

  }

  // title

  const title =
    document.getElementById(
      "questionTitle"
    )

  if (title) {

    title.innerText =
      q.question

  }

  // options

  const container =
    document.getElementById(
      "optionsContainer"
    )

  if (!container) return

  container.innerHTML = ""

  q.options.forEach(option => {

    const btn =
      document.createElement("button")

    btn.className =
      "option-btn"

    btn.innerText =
      option.option

    btn.onclick = () => {

      selectOption(option)

    }

    container.appendChild(btn)

  })

}


// ======================================
// SELECT OPTION
// ======================================

function selectOption(option) {

  state.selectedOption =
    option

  renderResult()

  showScreen("resultScreen")

}


// ======================================
// RENDER RESULT
// ======================================

function renderResult() {

  const option =
    state.selectedOption

  if (!option) return

  // фон

  const resultScreen =
    document.getElementById(
      "resultScreen"
    )

  if (resultScreen) {

    setBlurredBackground(
      resultScreen,
      state.currentQuestion.background
    )

  }

  // place

  const placeTitle =
    document.getElementById(
      "placeTitle"
    )

  if (placeTitle) {

    placeTitle.innerText =
      option.place

  }

  // text

  const resultText =
    document.getElementById(
      "resultText"
    )

  if (resultText) {

    resultText.innerText =
      option.resultText

  }

  // hint reset

  const hintBox =
    document.getElementById(
      "hintBox"
    )

  if (hintBox) {

    hintBox.innerText = ""

  }

  // hide next

  const nextBtn =
    document.getElementById(
      "nextBtn"
    )

  if (nextBtn) {

    nextBtn.style.display =
      "none"

  }

  // letters

  buildLetters(
    option.answer.trim()
  )

  // input

  const input =
    document.getElementById(
      "answerInput"
    )

  if (input) {

    input.value = ""

    input.focus()

  }

}


