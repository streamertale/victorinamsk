// OPEN NEXT QUESTION
// ======================================

function openNextQuestion() {

  const filteredQuestions =
    state.questions.filter(
      q => Number(q.day) === Number(state.currentDay)
    )

  const question =
    filteredQuestions[
      state.currentQuestionIndex
    ]

  // конец

  if (!question) {

    alert("Маршрут собран")

    console.log(
      "FINAL ROUTE:",
      state.selectedPlaces
    )

    return

  }

  state.currentQuestion =
    question

  renderQuestion()

  showScreen("questionScreen")

}


