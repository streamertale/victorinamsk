// ======================================
// BUILD ROUTE SCREEN
// ======================================

function buildRouteScreen() {

  const routeList =
    document.getElementById(
      "routeList"
    )

  if (!routeList) return

  // очистка

  routeList.innerHTML = ""

  // элементы маршрута

  state.selectedPlaces.forEach(item => {

    // card

    const card =
      document.createElement("div")

    card.className =
      "route-item"

    // question

    const question =
      document.createElement("div")

    question.className =
      "route-question"

    question.innerText =
      item.question

    // place

    const place =
      document.createElement("div")

    place.className =
      "route-place"

    place.innerText =
      item.place

    // append

    card.appendChild(question)

    card.appendChild(place)

    routeList.appendChild(card)

  })

}

