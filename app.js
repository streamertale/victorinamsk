// BUILD LETTERS
// ======================================

function buildLetters(word) {

  const row =
    document.getElementById(
      "lettersRow"
    )

  if (!row) return

  row.innerHTML = ""

  state.revealedLetters =
    Array(word.length).fill("")

  word.split("").forEach(() => {

    const box =
      document.createElement("div")

    box.className =
      "letter-box"

    row.appendChild(box)

  })

}


// ======================================
// RENDER LETTERS
// ======================================

function renderRevealedLetters() {

  const boxes =
    document.querySelectorAll(
      ".letter-box"
    )

  state.revealedLetters.forEach(
    (letter, index) => {

      if (boxes[index]) {

        boxes[index].innerText =
          letter

      }

    }
  )

}


// ======================================
// PROCESS INPUT
// ======================================

function processInput() {

  const input =
    document.getElementById(
      "answerInput"
    )

  if (!input) return

  const value =
    input.value
      .trim()
      .toLowerCase()

  if (!value) return

  const correctWord =
    state.selectedOption.answer
      .trim()
      .toLowerCase()

  // раскрываем буквы

  correctWord.split("").forEach(
    (letter, index) => {

      if (
        value.includes(letter)
      ) {

        state.revealedLetters[index] =
          letter

      }

    }
  )

  // render

  renderRevealedLetters()

  // очистить input

  input.value = ""

  // проверка

  const currentWord =
    state.revealedLetters.join("")

  if (currentWord === correctWord) {

    const nextBtn =
      document.getElementById(
        "nextBtn"
      )

    if (nextBtn) {

      nextBtn.style.display =
        "block"

    }

    // сохранить маршрут

state.selectedPlaces.push({

  question:
    state.currentQuestion.question,

  place:
    state.selectedOption.place

})

    console.log(
      "ROUTE:",
      state.selectedPlaces
    )

  }

}


